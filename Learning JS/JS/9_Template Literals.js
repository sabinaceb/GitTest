//TEMPLATE LITERALS
const firstName = 'John';
const lastName = 'Doe';
const age = 25;
console.log(firstName + " " + lastName + " is " + age + " years old"); // "John Doe is 25 years old"
console.log(`${firstName} ${lastName } is ${age} years old`); // "John Doe is 25 years old"

